int main() {
    printf("=== RiemannR accuracy (primesieve impl, MSVC double/long double) ===\n\n");

    // Check long double size
    printf("sizeof(double) = %zu, sizeof(long double) = %zu\n",
           sizeof(double), sizeof(long double));
    printf("double digits10 = %d, long double digits10 = %d\n\n",
           std::numeric_limits<double>::digits10,
           std::numeric_limits<long double>::digits10);

    printf("%20s %20s %22s %22s %14s %14s %12s\n",
           "n", "pi(n)", "R(double)", "R(long double)", "err_d", "err_ld", "rel_err_ld");

    for (auto& [n, pi] : ground_truth) {
        double rd = RiemannR((double)n);
        long double rld = RiemannR((long double)n);
        double err_d = rd - pi;
        double err_ld = (double)(rld - (long double)pi);
        double rel = err_ld / pi;

        printf("%20llu %20lld %22.1f %22.1Lf %14.1f %14.1f %12.2e\n",
               (unsigned long long)n, (long long)pi,
               rd, rld, err_d, err_ld, rel);
    }

    printf("\n=== RiemannR_inverse accuracy ===\n\n");
    printf("%20s %22s %22s %14s\n", "pi(n)", "R_inv(ld)", "actual_n", "err");

    for (auto& [n, pi] : ground_truth) {
        if (pi < 10) continue;
        long double inv = RiemannR_inverse((long double)pi);
        double err = (double)(inv - (long double)n);
        printf("%20lld %22.1Lf %22llu %14.1f\n",
               (long long)pi, inv, (unsigned long long)n, err);
    }

    return 0;
}
