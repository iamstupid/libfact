template <typename T>
T RiemannR(T x) {
    if (x < T(1e-5)) return 0;
    T epsilon = std::numeric_limits<T>::epsilon();
    T sum = 1, term = 1, logx = std::log(x);
    for (unsigned k = 1; k < 1000; k++) {
        term *= logx / k;
        T old_sum = sum;
        if (k + 1 < zeta.size())
            sum += term / (T(zeta[k + 1]) * k);
        else
            sum += term / k;
        if (std::abs(sum - old_sum) <= epsilon) break;
    }
    return sum;
}

template <typename T>
T RiemannR_inverse(T x) {
    if (x < 1) return 0;
    T logx = std::log(x);
    T loglogx = std::log(logx);
    T t = x * (logx + loglogx / 2);
    if (x > 1600) t = x * (logx + loglogx/2 + loglogx/2 - 1 + (loglogx-2)/logx);
    T old_term = std::numeric_limits<T>::infinity();
    for (int i = 0; i < 10; i++) {
        T term = (RiemannR(t) - x) * std::log(t);
        if (std::abs(term) >= std::abs(old_term)) break;
        t -= term;
        old_term = term;
    }
    return t;
}

